#include "3-calc.h"

/**
 * op_add - addition operation
 *
 * @a: first number
 * @b: second number
 *
 * Return: result of sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substraction operation
 *
 * @a: first number
 * @b: second number
 *
 * Return: the result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 *
 * @a: first number
 * @b: second number
 *
 * Return: the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_dev - division operation
 *
 * @a: first number
 * @b: second number
 *
 * Return: the result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulation operation
 *
 * @a: first number
 * @b: second number
 *
 * Return: the result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
